The whole initiative is self-sustaining in term of information gathering and require concerted efforts both by the NGO community and the volunteers who have the zeal to do something for the society.
<ul style="margin-left:50px">
<li>
At present, NGOs face challenges to gather active Volunteers who want to do volunteering
</li>
<li>
Volunteers do not have a proper platform to report their volunteering aspirations.
</li>
<li>
Volunteers also do not have access to one stop list of NGOs related to their field of interests.
</li>
NGOs do not have a single online platform to market themselves, report their activities and gather volunteers for new projects.
</li>
<li>
People in need for blood in emergency also do not have access to willing donors blood group information by location.
</li>
 </ul>
</br>
	This initiative overcome all the above problems in the following ways:
	 <ul style="margin-left:50px">
<li>
NGO register themselves through <a href = "<?php echo url::site(); ?>reports/submit?type=2" target= "_blank">NGO registration form</a>. Creating a login account is mandatory for NGOs. Once they create a login account, they get a facility to track their activities in the site once they login. Please note for NGOs registration, it will go through an approval process by our team once you register on the site.
</li>
<li>
Volunteers report their volunteering aspiration through <a href = "<?php echo url::site(); ?>reports/submit?type=2" target="_blank">Volunteer form</a>. Login is not mandatory but creating a login account will give the volunteers the facility to track their activities on the site.
</li>
<li>
NGO/Volunteers report their activities/initiatives through the <a href = "<?php echo url::site(); ?>reports/submit?type=3" target="_blank">Post activities form</a>
<li>
Volunteers report their blood group information during reporting their aspiration through the <a href = "<?php echo url::site(); ?>reports/submit?type=4" target="_blank">Report blood group form</a>
</li>
<li>
All the information are tagged on the map and can be searched and viewed at the click of the mouse in a visually appealing format. 
</li>
</ul>
